class Story:
    def __init__(self, id, title, summary, sentiment, entity, keyword, source_link, source_name, image_path):
        self.id = id
        self.title = title
        self.summary = summary
        self.sentiment = sentiment
        self.entity = entity
        self.keyword = keyword
        self.source_link = source_link
        self.source_name = source_name
        self.image_path = image_path
